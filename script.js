const defaultPoster = "https://encrypted-tbn0.gstatic.com/images?q=tbn:ANd9GcSmQJzitHoIGlOvdT952cPCLXQSF1_wo_A2gh4Wzcc9ZyIJhUjCVkEBoeg&s=10";

let currentChannel = 0;
let player = null;

function loadChannel(index) {
  const channel = channels[index];
  if (player) player.destroy();

  document.getElementById("player").style.opacity = 0;

  setTimeout(() => {
    player = new Clappr.Player({
      source: channel.url,
      parentId: "#player",
      poster: defaultPoster, // সব চ্যানেলের জন্য একই পোস্টার
      width: "100%",
      height: "100%",
      autoPlay: true,
      mute: false,
    });
    document.getElementById("player").style.opacity = 1;
  }, 300);
}

function renderChannelList() {
  const list = document.getElementById("channelList");
  channels.forEach((channel, index) => {
    const li = document.createElement("li");
    li.innerHTML = `<img src="${channel.logo}" alt=""> ${channel.name}`;
    li.onclick = () => {
      currentChannel = index;
      loadChannel(currentChannel);
    };
    list.appendChild(li);
  });
}

function nextChannel() {
  currentChannel = (currentChannel + 1) % channels.length;
  loadChannel(currentChannel);
}

function prevChannel() {
  currentChannel = (currentChannel - 1 + channels.length) % channels.length;
  loadChannel(currentChannel);
}

renderChannelList();
loadChannel(currentChannel);
